                      •Datos de contacto: nombre, dirección, correo electrónico y número de teléfono.<br>
                      •Información de pago: detalles de tarjetas de crédito/débito, métodos de pago preferidos.<br>
                      •Datos de navegación: cookies y direcciones IP para mejorar la experiencia del usuario.<br>
                      <b>2.2 Uso de Datos</b> <br>
                      Los datos se utilizan para:<br>
                      •Procesar y enviar pedidos.<br>
                      •Comunicaciones relacionadas con el pedido, como confirmaciones y actualizaciones.<br>
                      •Ofrecer promociones y descuentos personalizados.<br>
                      •Mejorar la experiencia del usuario mediante análisis y feedback.<br> <br>
              
                      <b style=color:black;>3. Protección de Datos Personales</b><br>
                      Decroche adopta medidas de seguridad para proteger la información personal de sus clientes, como:<br>
              
                          •Tecnología de encriptación SSL/TLS para transacciones seguras.<br>
                          •Acceso restringido a información sensible únicamente a personal autorizado.<br>
                          •Políticas de retención de datos para evitar almacenamiento innecesario de información personal.<br> <br>
              
